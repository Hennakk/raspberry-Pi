{

    if(wiringPiSetup () ==-1)            
        return -1;
    
    if(wiringPiSPISetup(CHANNEL, SPEED) == -1)
        return -1;

    for(int i=0; i<5; i++)           
    {
    wiringPiSPIDataRW(CHANNEL,initMax7219[i], 2);
   
 }

    while(1)
    {
        for(int j=0;j<18;j++)                  
        {
            WriteMax7219(characterData[j]);
            delay(500);
        }
  }

    return 0;
} 
